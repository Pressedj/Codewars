def even_or_odd(number):
    if number % 2 == 0:
        return "Even"
    else:
        return "Odd"
# What you see is what you get
# Line 2 sets the condition to check if the number has any remainders when divided by 2
# Line 3:  If it == 0 it means there are no remainders it returns Even
# Line 4: declares else statement
# Line 5: returns odd because its the only option if the number is not even
